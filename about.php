		el: '#app',
		data: {
			cat: 0,
		},
		computed: {},
		methods: {
			maketipClick() {
				var i = $(this);
				console.log(i);
				$(".maketipList li").on("click", function() {
					$(".maketipList li").not(this).removeClass('is-show');
					$(this).addClass('is-show');
				})

			},
			beforeEnter(el) {
				gsap.set(el, {
					opacity: 0
				});
			},
			enter(el, done) {
				gsap.to(el, .5, {
					opacity: 1,
					onComplete: () => {
						done()
						this.initHandler()
						this.enterHandler()
					}
				});
			},
			beforeLeave(el) {
				gsap.set(el, {
					opacity: 1
				});
			},
			leave(el, done) {
				gsap.to(el, .5, {
					opacity: 0,
					onComplete: done
				});
			},
			initHandler() {
				$(".rich-container-1 .qaList li").on("click", function() {
					$(this).toggleClass("is-open")
					$(".answer", this).slideToggle(300)
				})


				new Swiper('.wing-sliderWrap', {
					loop: true,
					speed: 500,
					slidesPerView: 1,
					spaceBetween: 0,
					centeredSlides: true,
					effect: 'fade',
					fadeEffect: {
						crossFade: true
					},
					autoplay: {
						delay: 4000,
						disableOnInteraction: false,
					},
					pagination: {
						el: '.swiper-pagination',
						clickable: true,
					},
					navigation: {
						nextEl: ".wing-next",
						prevEl: ".wing-prev",
					},
				});


				ScrollTrigger.refresh();
			},
			enterHandler() {
				var $deco1 = $(".mack168-container div[class^=deco]")
				var $deco2 = $(".maketip-container div[class^=deco]")
				var $deco3 = $(".wing-container div[class^=deco]")

				gsap.to($deco1, {
					duration: 1,
					opacity: 1,
					x: 0,
				})
				gsap.to($deco2, {
					duration: 1,
					opacity: 1,
					x: 0,
				})
				gsap.to($deco3, {
					duration: 1,
					opacity: 1,
					x: 0,
				})
			},
		},
		mounted() {
			this.initHandler();
			this.enterHandler();

		},

	})
</script>