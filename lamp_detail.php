	$('.controll-area .num-area .add').on("click", function() {
		var _num = parseInt($('.num-area .num').text());
		_num++;
		if (_num < 10) {
			$('.num-area .num').text('0' + _num);
		} else {
			$('.num-area .num').text(_num);
		}
	})
	$('.controll-area .num-area .minus').on("click", function() {
		var _num = parseInt($('.num-area .num').text());
		_num--;
		if (_num < 10) {

			if (_num < 0) {
				$('.num-area .num').text('00');
			} else {
				$('.num-area .num').text('0' + _num);
			}
		} else {
			$('.num-area .num').text(_num);
		}
	})
</script>